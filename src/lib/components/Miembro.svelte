        <summary>Detalles sobre Elena</summary>
        <p>
            Elena Felíu Arquiola es Catedrática de Universidad (Área de Lengua
            Española) en el Departamento de Filología Española de la Universidad
            de Jaén. Cuenta con cuatro sexenios (el último reconocido en 2022),
            cinco tramos autonómicos (máximo contemplado por la Junta de
            Andalucía) y cinco quinquenios de docencia. Es Doctora en Filosofía
            y Letras (Filología) por la Universidad Autónoma de Madrid (2001),
            Premio Extraordinario de Licenciatura y Premio Extraordinario de
            Doctorado. Fue becaria de colaboración del MECD y becaria FPU del
            MEC.
        </p>

        <p>
            Sus líneas de investigación se centran en la gramática del español,
            especialmente en la morfología y la teoría morfológica, así como en
            las relaciones de la morfología con la fonología, la sintaxis y la
            semántica. Destacan sus publicaciones sobre prefijación, sobre
            relaciones entre morfología y fonología en la creación de
            acortamientos, sobre construcciones medias y sobre concordancia
            anómala. Acerca de estos temas ha realizado aportaciones a la
            sociedad abarcan actividades tanto de transferencia como de
            divulgación. En cuanto a transferencia, ha colaborado en la revisión
            de la Nueva gramática de la lengua española de la RAE-ASALE para su
            segunda edición mediante la elaboración de un informe sobre el
            capítulo 10, “La prefijación”. En cuanto a su actividad divulgativa
            relacionada con la gramática del español y con la lingüística, ha
            escrito artículos en The Conversation y en Martes Neológico (Centro
            Virtual Cervantes) y ha participado en la Noche Europea de los
            Investigadores 2023 y en varias ediciones de la Semana de la Ciencia
            de la Universidad de Jaén.
        </p>
        <p>
            En relación con su trayectoria investigadora, cabe destacar su
            formación en el extranjero, que se refleja en la realización de
            varias estancias breves de investigación financiadas en distintos
            centros: Vrije Universiteit Amsterdam (Países Bajos), University of
            Essex (Reino Unido) y Université de Montréal (Canadá). Igualmente
            destaca su capacidad de trabajo en equipo, puesta de manifiesto en
            su participación como investigadora en once proyectos de
            investigación nacionales y autonómicos. Finalmente, en cuanto a su
            capacidad de liderazgo, cabe señalar que actualmente es la
            investigadora principal del proyecto de investigación Verbos
            pronominales del español: clasificación y alternancias (VERPRO)
            (ref. ID2023-149693NB-I00), financiado por el Ministerio de Ciencia,
            Innovación y Universidades. También ha sido investigadora principal
            del proyecto de excelencia Problemas de demarcación en morfología y
            sintaxis: diccionario de unidades y construcciones de difícil
            adscripción en español (ref. HUM673), financiado por la Consejería
            de Innovación, Ciencia y Empresa de la Junta de Andalucía
            (2014-2018), así como coordinadora durante cinco años del grupo de
            investigación Análisis lingüístico: teoría y aplicaciones (ALTYA)
            del Plan Andaluz de Investigación, Desarrollo e Innovación de la
            Junta de Andalucía (código HUM-834). Junto con David Serrano-Dolader
            (U. Zaragoza), ha coordinado desde 2012 los Encuentros de
            Morfólogos, que se celebran anualmente en España desde 2005.
        </p>
        <p>
            Los resultados de su investigación han visto la luz en editoriales y
            revistas de prestigio españolas y extranjeras. Es autora de las
            siguientes monografías: (con Enrique Pato) En torno a la denominada
            “concordancia adverbial” en español: tres casos de variación, 2022,
            CSIC, Anejos de la Revista de Filología Española (primer cuartil de
            las editoriales españolas de SPI) y Morfología derivativa y
            semántica léxica: la prefijación de auto-, co- e inter-, 2003, UAM
            Ediciones (cuarto cuartil de las editoriales españolas de SPI).
            Igualmente, es autora de numerosos artículos que han sido publicados
            en revistas como las siguientes: Languages (Q1 en Scimago Journal &
            Country Rank), Hispania (Q2 en Scimago Journal & Country Rank),
            Lengua y Habla (Q2 en Scimago Journal & Country Rank), Linguistics
            (Q2 en Scimago Journal & Country Rank), Onomázein (Q2 en Scimago
            Journal & Country Rank), Revista Signos. Estudios de Lingüística (Q2
            en Scimago Journal & Country Rank), Zeitschrift für romanische
            Philologie (Q2 en Scimago Journal & Country Rank), Estudios de
            Lingüística de la Universidad de Alicante (ELUA) (Sello FECYT,
            cuartil 2) y Verba (Sello FECYT, cuartil 2), entre otras.
        </p>
