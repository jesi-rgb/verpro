    <h2>Presentación</h2>
    <p>
        El proyecto de investigación “Verbos pronominales del español:
        <i>clasificación</i> y alternancias (<b>VERPRO</b>)" (ref.
        <code>ID2023-149693NB-I00</code>), financiado por el Ministerio de
        Ciencia, Innovación y Universidades, se propone ofrecer un estudio de
        conjunto exhaustivo de los verbos pronominales del español actual a
        partir de su organización en clases léxico-semánticas que permita
        evaluar los distintos factores (semánticos, gramaticales y pragmáticos)
        que parecen determinar tanto la presencia de alternancia (p. ej.
        <i>morir-morirse</i>) como su ausencia (p. ej.
        <i>fallecer/*fallecerse</i>
        y
        <i>suicidarse/*suicidar</i>).
    </p>

    <h2>Objetivos</h2>
    <ol>
        <li>Revisar y acotar el concepto de verbo pronominal.</li>
        <li>
            Recopilar los verbos que documentan marca pronominal en español
            actual y agruparlos dentro de su correspondiente clase
            léxico-semántica. Como punto de partida del proyecto, se recopilarán
            los verbos que aparecen en la base de datos ADESSE bajo las
            etiquetas de <i>construcción pronominal media</i> y construcción pronominal
            y se adscribirán esos verbos a las clases léxico-semánticas propuestas
            en la base de datos. Se atenderá especialmente a las siguientes 18 clases,
            elegidas bien por contener un mayor número de ejemplos, bien por contener
            un mayor número de verbos, bien por haber suscitado un especial interés
            en la bibliografía previa: verbos de cuidado del cuerpo (peinarse); de
            cambio de postura (sentarse); de contacto corporal (rascarse); de impacto
            (golpearse); de unión (recíprocos) (casarse, juntarse); de sensación
            (enfadarse); de conocimiento, creencia y cognición (aprenderse, creerse,
            pensarse); de desplazamiento (irse); de localización (quedarse); de manera
            de moverse (columpiarse); fisiológicos (orinarse); de comunicación (callarse);
            de modificación (cambio físico-químico) (quemarse); de percepción (verse);
            de creación (crearse); de posesión (tenerse); de transferencia (repartirse);
            de vida (morirse); de ingesta (beberse).
        </li>
        <li>
            Recoger muestras de uso contextualizado en CORPES XXI sobre la
            presencia o ausencia de la forma pronominal.
        </li>
        <li>
            Recopilar y describir las alternancias entre forma pronominal-no
            pronominal dentro de cada clase léxico-semántica. Se analizará tanto
            la presencia de alternancia (p. ej. morir/morirse) como su ausencia
            (verbos solo en activa, p. ej. fallecer, verbos solo pronominales,
            p. ej. suicidarse).
        </li>

        <li>
            Explicar dichas alternancias (y su ausencia) dentro de cada clase
            léxico-semántica con especial atención a lo siguiente
            <ul>
                <li>
                    Diferencias en el grado de control y afección del sujeto
                    (constructo de control).
                </li>
                <li>Diferencias en la valencia verbal.</li>
                <li>Diferencias en la Aktionsart y el tipo eventivo.</li>
                <li>
                    Diferencias pragmático-discursivas (estatus informativo de
                    la entidad del sujeto, afectividad del hablante, efectos
                    argumentativos, etc.).
                </li>
            </ul>
        </li>
        <li>
            Detectar posibles patrones de convergencia que permitan hablar de
            una función unitaria de la marca pronominal entre los verbos
            estudiados (o de un sistema de marcación), prestando especial
            atención a las siguientes cuestiones:
        </li>

        <li>
            Evaluar las distintas hipótesis explicativas en torno a las
            alternancias entre verbo pronominal y verbo no pronominal.
        </li>

        <li>
            Establecer posibles correlaciones entre la marcación pronominal del
            español y la de otras lenguas desde un enfoque tipológico.
        </li>
    </ol>
